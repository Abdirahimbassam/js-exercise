let a = 15;
let b = "15";

// equal
console.log(a == b);

// not equal
console.log(a != b);

// strictly equal to
console.log(a === b);

// strictly not equal to
console.log(a !== b);

// greater than
console.log(a > b);

// less than
console.log(a < b);

// greater than or equal
console.log(a >= b);

// less than or equal
console.log(a <= b);